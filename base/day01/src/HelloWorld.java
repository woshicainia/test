public class HelloWorld {//定义一个公开的HelloWorld类
    /*
    类体
     类体中不能直接编写Java语句（除声明变量）
    一个Java语句必须以；结束
     */
    //主方法是程序的入口，固定写法，args可
    public static void main(String[] args) {
        /*
         方法体
         在方法体中的代码，是遵循自上而下顺序依次执行的
         */
        //打印输出语句
        System.out.println("HelloWorld");
    }
}
