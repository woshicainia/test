public class HelloWorld {
    /*
     类体：
     类体中不能直接编写Java语句，除声明变量以外
     变量：在类体内定义的变量叫成员变量
    */
    public static void main(String[] args) {
        /*
        方法体：
        在方法体中的代码是自上问下依次执行的
        在方法体中定义的变量叫局部变量
         */
        System.out.println("HelloWorld");//HelloWorld
    }
}
