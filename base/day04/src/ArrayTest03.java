public class ArrayTest03 {
    public static void main(String[] args) {
        int[] arr = {1,2,3,4};//静态初始化
        //下面代码是一次访问数组中的元素
        System.out.println(arr[0]);//1
        System.out.println(arr[1]);//2
        System.out.println(arr[2]);//3
        System.out.println(arr[3]);//4
    }
}
