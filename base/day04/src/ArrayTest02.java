public class ArrayTest02 {
    public static void main(String[] args) {
        int[] arr = new int[3];
        arr[0] = 1;//为数组第一个元素赋值
        arr[1] = 2;//为元素第二个元素赋值
        arr[2] = 3;//为元素第三个元素赋值
        System.out.println(arr[0]);//1
        System.out.println(arr[1]);//2
        System.out.println(arr[2]);//3
    }
}
